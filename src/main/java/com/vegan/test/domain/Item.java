package com.vegan.test.domain;

public class Item {
private Integer id;
private int weight;
private int price;


public Integer getId() {
	return id;
}
public void setId(Integer id) {
	this.id = id;
}
public int getWeight() {
	return weight;
}
public void setWeight(int weight) {
	this.weight = weight;
}
public int getPrice() {
	return price;
}
public void setPrice(int price) {
	this.price = price;
}

}
